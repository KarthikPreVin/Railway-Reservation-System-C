int daysInMonth(int month, int year) {
    switch(month) {
        case 1: case 3: case 5: case 7: case 8: case 10: case 12:
            return 31;
        case 4: case 6: case 9: case 11:
            return 30;
        case 2:
            if ((year % 4 == 0 && year % 100 != 0) || (year % 400 == 0))
                return 29;
            else
                return 28;
        default:
            return -1; // Invalid month
    }
}

int dateToDays(struct DateTime date) {
    int days = 0;
    for (int y = 1; y < date.y; y++) {
        if ((y % 4 == 0 && y % 100 != 0) || (y % 400 == 0))
            days += 366;
        else
            days += 365;
    }
    for (int m = 1; m < date.m; m++) {
        days += daysInMonth(m, date.y);
    }
    days += date.d;
    return days;
}

int differenceInDays(struct DateTime date1, struct DateTime date2) {
    int days1 = dateToDays(date1);
    int days2 = dateToDays(date2);
    return days1 > days2 ? days1 - days2 : days2 - days1;
}

struct Ticket retrive_ticket(int Pnr,int *flag) {
    struct Ticket tik;
    tik.PNR=0;
    FILE *file = fopen("ticket_data.bin", "rb");
    if (file == NULL) {
        printf("Error opening file!\n");
        return tik;
    }

    while (fread(&tik, sizeof(struct Ticket), 1, file)) {
        if (tik.PNR == Pnr) {
            fclose(file);
            *flag=1;
            return tik;
        }
    }
    (*flag)++;
    fclose(file);
    struct Ticket empty;
    empty.PNR=0;
    return empty;
}
int update_train(struct Train train) {
    FILE *file = fopen("train_data.bin", "rb");
    struct Train* trains;
    struct Train temptrain;
    trains = (struct Train*)malloc(200 * sizeof(struct Train));
    int i = 0;
    int flag = 0;
    if (file == NULL) {
        printf("File not found\n");
        free(trains);
        return 2;
    }
    while (fread(&temptrain, sizeof(struct Train), 1, file)) {
        if (train.trainno == temptrain.trainno && temptrain.fromtime.y == train.fromtime.y && temptrain.fromtime.m == train.fromtime.m && temptrain.fromtime.d == train.fromtime.d) {
            trains[i] = train;
            i++;
            flag = 1;
            continue;
        }
        trains[i] = temptrain;
        i++;
    }
    fclose(file);
    file = fopen("train_data.bin", "wb");
    fwrite(trains, sizeof(struct Train), i, file);
    fclose(file);
    free(trains);
    return flag;
}
